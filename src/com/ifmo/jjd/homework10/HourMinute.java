package com.ifmo.jjd.homework10;

public class HourMinute {
    private long hours;
    private long minutes;

    public HourMinute(long hours, long minutes) {
        this.hours = hours;
        this.minutes = minutes;
    }

    public long getHours() {
        return hours;
    }

    public long getMinutes() {
        return minutes;
    }
}
